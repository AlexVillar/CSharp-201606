using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Clase2.Estructuras
{
    /// <summary>
    /// Una estructura es un conjunto pequeño de valores agrupados.
    /// Se utilizan en casos específicos, como para representar un area, coordenada geográfica o una fecha.
    /// Por lo general se prefiere el uso de clases en lugar de estructuras.
    /// Las estructuras no permiten herencia ni otras características propias de una clase.
    /// </summary>
    struct Estructura
    {
        int Valor1;

        int Valor2;

        int Valor3;
    }
}
